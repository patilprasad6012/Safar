const internalI18n = {
    init: function() {
        this.currentLang = localStorage.getItem('safar_lang') || 'en';
        this.bindSwitcher();
        
        if (this.currentLang !== 'en') {
            this.translatePage();
            // Optional: Watch for dynamic DOM changes (like modals opening)
            const observer = new MutationObserver((mutations) => {
                let shouldTranslate = false;
                mutations.forEach(m => {
                    if (m.addedNodes.length > 0) shouldTranslate = true;
                });
                if (shouldTranslate) this.translatePage();
            });
            observer.observe(document.body, { childList: true, subtree: true });
        }
    },

    bindSwitcher: function() {
        const langBtns = document.querySelectorAll('.lang-btn');
        if (!langBtns.length) return;

        // Apply active class to the current button
        langBtns.forEach(btn => {
            if (btn.getAttribute('data-lang-code') === this.currentLang) {
                btn.classList.add('active', 'fw-bold');
                // Optional: Append a checkmark to show it is selected
                if (!btn.querySelector('.check-icon')) {
                    btn.innerHTML += '<i data-lucide="check" size="18" class="check-icon"></i>';
                }
            } else {
                btn.classList.remove('active', 'fw-bold');
                const check = btn.querySelector('.check-icon');
                if (check) check.remove();
            }

            btn.addEventListener('click', (e) => {
                const newLang = btn.getAttribute('data-lang-code');
                if (newLang && newLang !== this.currentLang) {
                    localStorage.setItem('safar_lang', newLang);
                    window.location.reload();
                }
            });
        });
        
        // Theme switch functional mock (bonus setting requested by user's prompt interpretation)
        const themeSwitch = document.getElementById('themeSwitch');
        if (themeSwitch) {
            const isDark = localStorage.getItem('safar_theme') === 'dark';
            themeSwitch.checked = isDark;
            if (isDark) {
                document.documentElement.setAttribute('data-bs-theme', 'dark');
                document.body.classList.add('dark-theme-native'); // Custom additions
            }
            
            themeSwitch.addEventListener('change', (e) => {
                if (e.target.checked) {
                    localStorage.setItem('safar_theme', 'dark');
                    document.documentElement.setAttribute('data-bs-theme', 'dark');
                    document.body.classList.add('dark-theme-native');
                } else {
                    localStorage.setItem('safar_theme', 'light');
                    document.documentElement.setAttribute('data-bs-theme', 'light');
                    document.body.classList.remove('dark-theme-native');
                }
            });
        }
    },

    translatePage: function() {
        const dict = translations[this.currentLang];
        if (!dict) return;

        // Translate Text Nodes
        const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
            acceptNode: function(node) {
                if (node.parentElement && ['SCRIPT', 'STYLE'].includes(node.parentElement.tagName)) {
                    return NodeFilter.FILTER_REJECT;
                }
                const text = node.nodeValue.trim();
                // Filter out large blocks, numbers, symbols to optimize
                if (text.length > 1 && text.length < 100) return NodeFilter.FILTER_ACCEPT;
                return NodeFilter.FILTER_SKIP;
            }
        });

        const nodesToTranslate = [];
        while (walker.nextNode()) {
            nodesToTranslate.push(walker.currentNode);
        }

        // Sort dictionary keys by length (longest first) to prevent partial replacement bugs
        const sortedEntries = Object.entries(dict).sort((a, b) => b[0].length - a[0].length);

        nodesToTranslate.forEach(node => {
            const originalText = node.nodeValue.trim();
            if (!originalText) return;

            // Direct dictionary lookup
            if (dict[originalText]) {
                node.nodeValue = node.nodeValue.replace(originalText, dict[originalText]);
            } else {
                // Heuristic lookup longest first
                for (const [eng, trn] of sortedEntries) {
                    if (node.nodeValue.includes(eng)) {
                        node.nodeValue = node.nodeValue.replace(eng, trn);
                    }
                }
            }
        });

        // Translate Placeholders, Titles, and Values (for inputs like buttons)
        document.querySelectorAll('[placeholder], [title], input[type="submit"], input[type="button"]').forEach(el => {
            if (el.placeholder && dict[el.placeholder]) el.placeholder = dict[el.placeholder];
            if (el.title && dict[el.title]) el.title = dict[el.title];
            if (el.value && dict[el.value]) el.value = dict[el.value];
        });
    }
};

document.addEventListener('DOMContentLoaded', () => internalI18n.init());
